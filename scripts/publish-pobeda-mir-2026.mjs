async function main() {
  console.log(`→ ${article.title}`)
  const { data: existing } = await supabase.from('articles').select('id').eq('slug', article.slug).maybeSingle()
  if (existing) { console.log('  ! слаг уже занят'); return }

  const buf = await sharp(Buffer.from(svgPobeda(article.title))).resize(1200, 675).webp({ quality: 88 }).toBuffer()
  const file = `styled-${article.slug.slice(0, 30)}-${Date.now()}.webp`
  const { error: upErr } = await supabase.storage.from('covers').upload(file, buf, { contentType: 'image/webp' })
  if (upErr) { console.log('  ✗ обложка:', upErr.message); return }
  const cover = supabase.storage.from('covers').getPublicUrl(file).data.publicUrl

  const { error: insErr } = await supabase.from('articles').insert({
    title: article.title, slug: article.slug, lead: article.lead, content: article.content,
    cover_image: cover, category_id: article.category_id, status: 'published',
    is_featured: article.is_featured, published_at: article.published_at,
  })
  if (insErr) { console.log('  ✗ вставка:', insErr.message); return }
  console.log('  ✓ опубликовано (featured)')
  console.log('  cover:', cover)
}
main().catch(e => { console.error(e); process.exit(1) })
